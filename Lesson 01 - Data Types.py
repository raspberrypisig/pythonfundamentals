#Let's learn Python

#The perennial Hello World
print("Hello World")

#Some data types available in Python

#--Primitive data types
print("hello double quotes") #string
print('hello single quotes') #also a string
print('''line one
line two
line three''') #multiline string
print(4) #integer
print(4.5) #float
print(True) #boolean

#--Collection data types
print([1,2,3]) #list
print((1,2,3)) #tuple
print({1,2,3}) #set
print({"convenor": "Mohan", "co-convener":"Rob"}) #dictionary

